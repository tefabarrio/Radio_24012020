    private void CambiarFmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CambiarFmActionPerformed
        jToggleButton1.setEnabled(false);
        boolean x;
        if(frecuenciatxt.getText().equals("AM")){
            x=true;
        }
        else{
            x=false;
        }
        miRadio.setFrecuencia(x);
        miRadio.cambiarFrecuencia();
        x=miRadio.getFrecuencia();
        if(x==true){
            frecuenciatxt.setText("AM");
        }
        else{
            frecuenciatxt.setText("FM");
        }
        
    }//GEN-LAST:event_CambiarFmActionPerformed

